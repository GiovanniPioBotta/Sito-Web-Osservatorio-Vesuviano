<font color="RED"><h1> Che cos'è un vulcano</h1></font>
</td></tr>
	  <tr>
<td><br><style="text-decoration: none;"><font size="4" face="Helvetica"> Un vulcano è una struttura geologica molto complessa, generata all'interno della crosta terrestre dalla risalita, 
     in seguito ad attività eruttiva, di massa rocciosa fusa, il magma, formatasi al di sotto o all'interno della crosta terrestre. 
	 È formato da una struttura non visibile, interna alla crosta, e che comprende la camera magmatica e i condotti magmatici,
	 e una struttura visibile esterna formata dal rilievo vulcanico, generalmente più o meno conico, formato dall'accumulo dei materiali liquidi, 
	 solidi o gassosi che sono stati emessi dal cratere vulcanico o dai crateri durante le varie fasi eruttive del vulcano stesso. 
	 Più in generale sono considerati vulcani tutte le discontinuità nella crosta terrestre attraverso le quali, con manifestazioni varie,
	 si fanno strada i prodotti dell'attività magmatica endogena: polveri, gas, vapori e materiali fusi solidi.</font></style><br><br></tr></table>
	 
