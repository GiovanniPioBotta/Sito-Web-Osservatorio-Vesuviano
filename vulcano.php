<td><br><style="text-decoration: none;"><font size="4" face="Helvetica"> Il Dipartimento Vulcani conduce le proprie
ricerche su sei diverse aree tematiche, la cui descrizione è riportata nei link sottostanti. 
Le Aree tematiche individuate sono tutte caratterizzate da un’impronta multidisciplinare e aggregante 
rispetto alle diverse metodologie di indagine impiegate nella ricerca.
Allo stesso tempo queste Aree tematiche sono complementari e funzionali le une alle altre,
con l’obiettivo di descrivere i processi vulcanici (fisici e chimici) nella loro interezza e
nel modo più quantitativo, coerente e completo possibile.<br>

Alle tematiche strettamente vulcanologiche si aggiungono poi due tematiche trasversali,
ovvero comuni ai tre Dipartimenti denominate: 1) Ricostruzione e modellazione della struttura crostale e 2)
Studi per le georisorse.<br></br>


-Storia eruttiva <br>

-Struttura e sistema di alimentazione dei vulcani <br>

-Proprietà chimico-fisiche dei magmi e dei prodotti vulcanici <br>

-Processi pre-eruttivi <br>

-Processi eruttivi e post-eruttivi <br>

-Pericolosità vulcanica e contributi alla stima del rischio <br><br>

Una settima linea di attività è rappresentata da ricerche, studi e azioni complessivamente volte
a minimizzare i pericoli naturali, a favorire la crescita delle conoscenze vulcanologiche, a promuovere
uno sviluppo più sicuro e armonico tra ambienti vulcanicamente attivi e i cittadini che li abitano:
