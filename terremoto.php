<font color="RED"><h1> Monitoraggio sismico </h1></font>
</td></tr>
	  <tr>
	<td><br><style="text-decoration: none;"><font size="4" face="Helvetica"> Il monitoraggio dei terremoti, tramite le proprie reti di monitoraggio 
	multidisciplinari raccoglie dati su una vasta gamma di fenomeni geofisici e utilizza tali dati per i propri studi. 
	Attività di particolare rilievo sono la promozione dell’innovazione nel monitoraggio, nella raccolta dei dati,
	nella loro analisi in tempo reale. Il Dipartimento Terremoti è responsabile del monitoraggio sismico a scala
	nazionale e degli tsunami che avvengono nel bacino del Mediterraneo, sui quali fornisce informazioni tempestive
	agli organi governativi e alla società. Pubblica bollettini e rapporti dettagliati sull'attività sismica,
	gestisce le banche dati sismologiche liberamente accessibili con informazioni sui terremoti storici e quelli strumentali.  
	I dati raccolti sono utilizzati per studiare la natura e la distribuzione dell'attività sismica, le cause e
	il contesto geodinamico di riferimento, per migliorare la comprensione del rischio sismico. L’informazione ottenuta
	dalle onde sismiche registrate è utilizzata per immaginare l’interno della Terra e sviluppare modelli sulla struttura terrestre. <br><br>
	
	Le attività di ricerca abbracciano una pluralità di competenze e metodi e sono raggruppate in otto aree tematiche, in alcuni casi
	fortemente interconnesse e in continuo dialogo tra di loro.<br><br> 

• Struttura della Terra <br>
• Deformazione crostale attiva <br>
• Sorgente Sismica <br>
• Sismicità dell’Italia <br>
• Sismologia, geofisica e geologia per l’ingegneria sismica <br>
• Studi di pericolosità sismica e da maremoto <br>
• Variazioni delle caratteristiche crostali e precursori sismici <br>
• Sismologia in Tempo Reale <br><br>

Dalla ricerca sempre in via di sviluppo nascono prodotti e attività che hanno una ricaduta sulla società tale da diventare
servizi continuativi. La Linea di Attività Servizi e Ricerca per la Società se ne occupa, favorendone il continuo aggiornamento,
come anello di interscambio tra la Ricerca di base e la società.
