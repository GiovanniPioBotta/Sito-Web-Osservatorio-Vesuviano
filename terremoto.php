<font color="RED"><h1> Cosa sono i Terremoti </h1></font>
</td></tr>
	  <tr>
	  
<td><br><style="text-decoration: none;"><font size="4" face="Helvetica"> I terremoti sono vibrazioni o assestamenti della crosta terrestre, 
 provocati dallo spostamento improvviso di una massa rocciosa nel sottosuolo.
 Tale spostamento è generato dalle forze di natura tettonica che agiscono costantemente all'interno della crosta terrestre provocando
 una lenta deformazione fino al raggiungimento del carico di rottura con conseguente liberazione di energia elastica in una zona
 interna della Terra detta ipocentro, tipicamente localizzato in corrispondenza di fratture preesistenti della crosta dette faglie;
 a partire dalla frattura creatasi una serie di onde elastiche, dette onde sismiche, si propagano in tutte le direzioni dall'ipocentro,
 dando vita al fenomeno osservato in superficie con il luogo della superficie terrestre posto sulla verticale dell'ipocentro,
 detto epicentro, che è generalmente quello più interessato dal fenomeno. La branca della geofisica che studia questi fenomeni è la sismologia.<br><br>
