<h3> Bollettino Vesuvio </h3>
Durante il mese di aprile 2021 l’attività del Vesuvio si è mantenuta su livelli bassi. Sono stati registrati 84 terremoti con una Magnitudo massima=1.9±0.3. 
Di questi, 75 eventi (pari al 88% del totale) hanno avuto una magnitudo minore di 1.0, 7 eventi (pari al 8%) hanno avuto una magnitudo compresa tra 1.0 e 1.5 e 
1 evento ha avuto una magnitudo maggiore di 1.5, confermando un livello di attività sismica di bassa energia. Nel complesso, è stato possibile localizzare 68 
terremoti (81% di quelli registrati) ubicati prevalentemente in area craterica con profondità che non superano 2.0 chilometri. I dati delle reti di monitoraggio 
geodetico non evidenziano deformazioni riconducibili a sorgenti vulcaniche ma solo ad effetti gravitativi, a processi di compattazione del materiale incoerente 
superficiale o a effetti locali che possono influenzare l'inclinazione del suolo. Le misure termografiche effettuate in area craterica mostrano un andamento 
stazionario della temperatura massima. Le analisi geochimiche effettuate sul bordo e sul fondo del cratere non evidenziano variazioni significative.
<br><br>

<h3> Bollettino Campi Flegrei </h3>
Durante il mese di aprile 2021 nell'area dei Campi Flegrei sono stati registrati 220 terremoti con una Magnitudo massima=1.9±0.3. 198 eventi (90%) 
hanno avuto una magnitudo minore di 1.0, a dimostrazione di un livello di sismicità con energia generalmente molto bassa; 3 eventi hanno avuto una magnitudo 
compresa tra 1.0 e 1.5, mentre solo un evento ha avuto una magnitudo maggiore o uguale a 1.5. Di 18 eventi non è stato possibile determinare la magnitudo a causa 
della bassa ampiezza del segnale non chiaramente distinguibile dal rumore di fondo. In totale sono stati localizzati 98 eventi (45% di quelli registrati) ubicati 
prevalentemente tra Pozzuoli, l'area Solfatara-Pisciarelli-Agnano e il Golfo di Pozzuoli con profondità fino a 3.6 km. Le reti di monitoraggio delle deformazioni 
del suolo confermano una geometria radiale del sollevamento centrato nell'area di Pozzuoli con una velocità media di circa 13±2 mm/mese. Il sollevamento registrato 
alla stazione GPS di Rione Terra (RITE) a partire da gennaio 2016 è di circa 46 cm. Le misure termografiche nelle aree monitorate mostrano andamenti della 
temperatura sostanzialmente stabili. I parametri geochimici confermano i trend in aumento già osservati in precedenza.

<h3> Bollettino Ischia </h3>
Nel mese di aprile 2021 non si osservano particolari dinamiche all'Isola d'Ischia. Non sono stati registrati terremoti. Dal punto di vista deformativo, 
l'isola è caratterizzata da una generale subsidenza più evidente nel settore meridionale con una velocità media alla stazione di Serrara Fontana di circa 
-6.5 mm/anno; l'inclinazione del suolo, misurata dai tre sensori in pozzo, mostra una direzione prevalente verso N. Le misure con termocamere mobili 
effettuate in diversi punti dell’Isola d’Ischia mostrano andamenti stabili.

